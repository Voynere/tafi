?><h1 align="center">Методика забора биосубстратов (волосы, ногти) для определения химических элементов. </h1>
<h3>ВОЛОСЫ:</h3>
<p>
	 Для проведения анализа необходима прядь волос длиной до 3-5 см (непосредственно от корня волос). Волосы состригают в 4-5 местах на затылке, ближе к шее и далее объединяют в пучок толщиной 2-3 мм. Коротких волос необходимо количество, способное заполнить чайную ложку. Волосы должны быть чистыми, без лака, геля, жидкости для укладки и пр. Химическая завивка, окраска и обесцвечивание не являются затруднением для анализа, однако об этом необходимо сообщить в прилагаемой анкете, указав также естественный цвет волос. Волосы необходимо вложить в отдельный бумажный конверт (можно постовый) (не использовать алюминиевую фольгу и полиэтиленовые пакеты), отметив корневой конец пряди. Специальных условий хранения и транспортировки не требуют.
</p>
<p>
	 При необходимости возможен отбор волос с других частей тела (с груди, лобка). В этом случае в сопроводительных документах делается соответствующая отметка.
</p>
<p>
	 Для проведения анализа «Алопеция» волосы состригаются с затылочной зоны (см. первый абзац) и с теменной зоны. Для среза волос с теменной зоны проводится условная линия, соединяющая левый и правый виски по кратчайшему расстоянию, проведенному через теменную зону. Посередине этой линии состригают пучок волос толщиной 2-3 мм. Коротких волос необходимо количество, способное заполнить ½ чайной ложки.
</p>
<h3>
Важно!&nbsp;</h3>
<p>
	 Если при заборе проб используют перчатки, то они должны быть не опудренные и не содержать латекса (напр., нитриловые)
</p>
<h3>
Примечание:</h3>
<p>
	 Рекомендуется прекратить использование лечебных шампуней (против перхоти, содержащих селен или цинк) за 1-2 недели до сдачи волос на анализ.
</p>
<h3>НОГТИ:&nbsp;</h3>
<p>
	 Для проведения анализа ногтей необходимо срезать ногти со всех пальцев обеих рук или ног. Длина срезаемых ногтей должна быть не менее 2-х мм. Необходимо указать, откуда (с рук или ног) взяты ногти.&nbsp;Специальных условий хранения и транспортировки волосы и ногти не требуют.
</p>
<h3>Важно! </h3>
 Ногти принимаются на анализ только при отсутствии волос.<br>

